# Type Conversion in Python
# Type Conversion in Python allows users to convert int type to another, for example, float to int, int to complex, int to float, etc.

# Convert int to float

val = 5
print("Value: ", val)
res = float(val)
print("Conversion to float: ",res)
res1 = int(res)
print("Conversion to int: ",res1)
res2 = complex(res1)
print("Conversion to complex: ",res2)